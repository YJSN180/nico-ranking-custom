// 人気タグを取得（将来的にはKVから動的に取得）
export function getPopularTags(genre: RankingGenre, limit: number = 15): string[] {
  const tags = CURATED_POPULAR_TAGS[genre] || CURATED_POPULAR_TAGS.all
  return tags.slice(0, limit)
}

// タグが有効かチェック
export function isValidTag(tag: string): boolean {
  // 空文字やR-18を含むタグは無効
  return tag.length > 0 && !tag.includes('R-18')
}